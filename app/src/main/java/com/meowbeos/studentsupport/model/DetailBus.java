public class DetailBus {
    private int d;
    private String v;
    private String s;
    private int t;

    public int getD() {
        return d;
    }

    public void setD(int d) {
        this.d = d;
    }

    public String getV() {
        return v;
    }

    public void setV(String v) {
        this.v = v;
    }

    public String getS() {
        return s;
    }

    public void setS(String s) {
        this.s = s;
    }

    public int getT() {
        return t;
    }

    public void setT(int t) {
        this.t = t;
    }
}
